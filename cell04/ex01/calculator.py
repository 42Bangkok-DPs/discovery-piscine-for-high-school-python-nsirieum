#!/usr/bin/env python3
a = int(input("Give me the first number: "))
b = int(input("Give me the second number: "))
print("Thank you!")
add = a+b
print(str(a) + ' + ' + str(b) + " = " ,add)
minus = a-b
print(str(a) + ' - ' + str(b) + " = " ,minus)
divide = a/b
print(str(a) + ' / ' + str(b) + " = " ,divide)
multi = a*b
print(str(a) + ' * ' + str(b) + " = " ,multi)